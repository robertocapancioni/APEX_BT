drop table d09_error_lookup cascade constraints;

create table d09_error_lookup (
    id                             number generated by default on null as identity 
                                   constraint d09_error_lookup_id_pk primary key,
    constraint_name                varchar2(30 char) not null,
    message                        varchar2(4000 char) not null,
    created                        date not null,
    created_by                     varchar2(255 char) not null,
    updated                        date not null,
    updated_by                     varchar2(255 char) not null
)
;


create or replace trigger d09_error_lookup_biu
    before insert or update 
    on d09_error_lookup
    for each row
begin
    if inserting then
        :new.created := sysdate;
        :new.created_by := coalesce(sys_context('APEX$SESSION','APP_USER'),user);
    end if;
    :new.updated := sysdate;
    :new.updated_by := coalesce(sys_context('APEX$SESSION','APP_USER'),user);
end d09_error_lookup_biu;
/

